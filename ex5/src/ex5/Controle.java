package ex5;

import java.util.Scanner;

public class Controle {

	public static String leString() {

		Scanner scan = new Scanner(System.in);
		String ler = scan.next();

		return ler;
	}

	public static int leInteiro() {

		Scanner scan = new Scanner(System.in);
		int add = scan.nextInt();

		return add;
	}

}
